package Fundamentos.Operadores;

public class OperadoresUnarios {
    public static void main(String[] args) {
        
        int a = 1;
        
        //incremento pos fixado
        a++; // a = a + 1 

        //decremento pos fixado
        a--; // a = a - 1 

        //incremento pre fixado
        ++a;

        //decremento pre fixado
        --a;

        System.out.println(a);
    }
}
